name = "ada lovelace"
print(name.title()) # "title" case is a method that capitalizes the first letter of each word in a string.
print(name.upper()) # "upper" case is a method that capitalizes all letters of each word in a string.
print(name.lower()) # "title" case is a method that makes lower case all letters of each word in a string.

name = "ada"
surname = "lovelace"
full_name = name + " " + surname + "!" # concatenation of strings
print(full_name) # "ada lovelace"
print(full_name.title()) # "Ada Lovelace"